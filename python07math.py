print("EJEMPLO LIBRERIA PYTHON")
# Sintaxis con FROM
from math import floor, ceil, trunc
numero1 = 20
numero2 = 3
division = numero1 / numero2
print("el resultado de la division es: " + str(division))
# Vamos a utilizar los metodos de la libreria
varFloor = floor(division)
varCeil = ceil(division)
varTrunc = trunc(division)
print ("Floor: " + str(varFloor))
print ("Ceil: " + str(varCeil))
print ("Trunc: " + str(varTrunc))
print ("FIN DE PROGRAMA")